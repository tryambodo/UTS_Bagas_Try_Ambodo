    private void selectData() {
        String kolom[] = {"Nama_Pemesan,No_ID,Stasion_Awal,Stasion_Tujuan,Tanggal_Berangkat,Jenis_Kereta,Waktu_Berangkat,Tempat_Duduk,Harga"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_pesan";
        ResultSet rs = koneksiDB.executeQuery(SQL);
        try {
            while(rs.next()) {
                String Nama_Pemesan = rs.getString(1);
                String No_ID = rs.getString(2);
                String Stasion_Awal = rs.getString(3);
                String Stasion_Tujuan = rs.getString(4);
                String Tanggal_Berangkat = rs.getString(5);
                String Jenis_Kereta = rs.getString(6);
                String Waktu_Berangkat = rs.getString(7);
                String Tempat_Duduk = rs.getString(8);
                String Harga = rs.getString(9);
                String data[] = {Nama_Pemesan,No_ID,Stasion_Awal,Stasion_Tujuan,Tanggal_Berangkat,Jenis_Kereta,Waktu_Berangkat,Tempat_Duduk,Harga};
                dtm.addRow(data);
            }
        }catch (SQLException ex){
            Logger.getLogger(Frame_Pemesanan.class).log(Level.SEVERE, null, ex);
        }
        tblData.setModel(dtm);
        
    }
