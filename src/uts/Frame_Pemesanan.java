    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
                            //Tanggal
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd"); 
        String tanggal = dateFormat.format(dateTanggal.getDate());
                            //Input Data
        if("".equals(txtNama.getText()) || "".equals(txtKTP.getText()) || "".equals(txtStasiunAwal.getText()) || "".equals(txtWaktu.getText()) || tanggal.equals("") || "".equals(txtJK.getText()) || "".equals(txtWaktu.getText()) || "".equals(cbTempatDuduk.getSelectedItem()) || "".equals(txtHarga.getText())){
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            String SQL = "INSERT INTO tb_pesan (Nama_Pemesan,No_ID,Stasion_Awal,Stasion_Tujuan,Tanggal_Berangkat,Jenis_Kereta,Waktu_Berangkat,Tempat_Duduk,Harga)"
                    + "VALUES('"+txtNama.getText()+"','"+txtKTP.getText()+"','"+txtStasiunAwal.getText()+"','"+txtStasiunTujuan.getText()+"',"
                    + "'"+tanggal+"','"+txtJK.getText()+"','"+txtWaktu.getText()+"','"+cbTempatDuduk.getSelectedItem()+"','"+txtHarga.getText()+"')";
            int status = koneksiDB.execute(SQL);
            if (status == 1){
                JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }else{
                JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan!", "Error", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

